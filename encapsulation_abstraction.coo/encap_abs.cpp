/*

Encapsulation:

Encapsulation ka mtlb hota hai ki aap apne data ko ek unit ke
 andar bandh kar rakhe, jise hum class kehte hain.
Yani ki, aap apne data (variables) aur uske sath wale functions (methods) 
ko ek unit mein organize karte hain. Is unit ko hum class kehte hain.
Encapsulation ka main fayda yeh hai ki aap apne data ko protect kar sakte hain,
 jise direct access nahi kiya ja sake bina us class ke.


Abstraction:

Abstraction ka mtlb hota hai ki aap sirf woh details dikhate hain 
jo zaroori hain aur baaki ko chhupa dete hain.
Yani ki, aap apne data ke saath wale functions ko leke itna detail
 nahi dikhate, bas woh information dikhate hain jo user ko zarurat hai.
Abstraction ka use karke aap apne code ko simple banaye rakh sakte 
hain aur unnecessary complexity ko chhupa sakte hain.

*/